my_input = int(input("Enter a number: "))

my_operation = my_input / 10.8

print(f'The result is: {my_operation}')

# Use ~ :.<numbers that we want after the point>f ~ for formatting float numbers.
print(f'The result is: {my_operation:.4f}')
print(f'The result is: {my_operation:.2f}')
print(f'The result is: {my_operation:.1f}')
print(f'The result is: {my_operation:.0f}')
